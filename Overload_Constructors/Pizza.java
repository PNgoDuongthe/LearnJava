package Overload_Constructors;

public class Pizza {
    String bread;
    String sauce;
    String cheese;
    String topping;
     Pizza(String Bread,String Sauce,String Cheese,String Topping){
         this.bread =Bread;
         this.sauce = Sauce;
         this.cheese= Cheese;
         this.topping = Topping;
     }
}
